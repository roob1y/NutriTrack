export function categorise(name) {
  const lower = name.toLowerCase();
  for (const cat of CATEGORIES) {
    if (cat.keywords.some((kw) => {
      const regex = new RegExp(`\\b${kw}s?\\b`);
      return regex.test(lower);
    })) {
      return cat.label;
    }
  }
  return 'Other';
}